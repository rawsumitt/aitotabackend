const mongoose = require('mongoose');

const BusinessInfoSchema = new mongoose.Schema({
    clientId: {
        type: String,
        required: true
    },
    text: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    updatedAt: {
        type: Date,
        default: Date.now
    }
});

module.exports = mongoose.model('BusinessInfo', BusinessInfoSchema);